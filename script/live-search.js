const searchInput = document.getElementById("liveSearch");
const suggestionsList = document.getElementById("suggestions");

searchInput.addEventListener("input", () => {
    const input = searchInput.value.toLowerCase().trim();
    suggestionsList.innerHTML = "";

    if (input === "") return;

    const countries = Object.keys(recipeCountries).filter(country =>
        country.toLowerCase().startsWith(input)
    );

    const recipeCards = Object.entries(recipeCountries)
        .flatMap(([country, recipes]) => recipes.map(r => ({ ...r, country })))
        .filter(recipe => recipe.name.toLowerCase().startsWith(input));

    const detailedPage = detailedRecipes.filter(recipe =>
        recipe.name.toLowerCase().startsWith(input)
    );

    const combinedResults = [
        ...countries.map(country => ({ type: "country", name: country })),
        ...detailedPage.map(r => ({ type: "detailed", name: r.name, id: r.id })),
        ...recipeCards.map(r => ({ type: "recipe", name: r.name }))
    ];

const unique = new Map();
combinedResults.forEach(item => {
    const key = item.type === "detailed" ? `${item.name}-${item.id}` : item.name.toLowerCase(); // Use a unique key for detailed recipes
    if (!unique.has(key)) {
        unique.set(key, item);
    }
});

    [...unique.values()].forEach(result => {
        const li = document.createElement("li");
        li.textContent = result.name;

        li.addEventListener("click", () => {
            if (result.type === "country") {
                window.location.href = `overview-recipe-page.html?country=${encodeURIComponent(result.name)}`;
            } else {
                window.location.href = `sambusa.html?id=${encodeURIComponent(result.id)}`;
            }
        });

        suggestionsList.appendChild(li);
    });
});

searchInput.addEventListener("keydown", (e) => {
    if (e.key === "Enter") {
        const firstSuggestion = suggestionsList.querySelector("li");
        if (firstSuggestion) firstSuggestion.click();
    }
});

